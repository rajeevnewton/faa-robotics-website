<script setup>
import { ref, onMounted, onUnmounted } from 'vue'
import Footer from '../components/Footer.vue'

const flippedCards = ref(new Set())
const activeStep = ref(1)
const carouselIntervals = []

const toggleCard = (cardId) => {
  if (flippedCards.value.has(cardId)) {
    flippedCards.value.delete(cardId)
  } else {
    flippedCards.value.add(cardId)
  }
}

const setActiveStep = (step) => {
  activeStep.value = step
}

const bookMeeting = () => {
  window.open('https://api.whatsapp.com/send/?phone=%2B919347206123', '_blank')
}

// Lab Image Carousel Logic
const startCarousels = () => {
  const carousels = document.querySelectorAll('.lab-image-carousel')
  
  carousels.forEach((carousel) => {
    const images = carousel.querySelectorAll('.lab-img')
    if (images.length > 1) {
      let currentIndex = 0
      
      const interval = setInterval(() => {
        images[currentIndex].classList.remove('active')
        currentIndex = (currentIndex + 1) % images.length
        images[currentIndex].classList.add('active')
      }, 3000) // Change image every 3 seconds
      
      carouselIntervals.push(interval)
    }
  })
}

onMounted(() => {
  startCarousels()
})

onUnmounted(() => {
  carouselIntervals.forEach(interval => clearInterval(interval))
})
</script>

